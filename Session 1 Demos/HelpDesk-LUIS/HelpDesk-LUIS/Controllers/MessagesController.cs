            {
                // Implement user deletion here
                // If we handle user deletion, return a real message
            }
            else if (message.Type == ActivityTypes.ConversationUpdate)
            {
                // Handle conversation state changes, like members being added and removed
                // Use Activity.MembersAdded and Activity.MembersRemoved and Activity.Action for info
                // Not available in all channels

                if (message.MembersAdded.Any(o => o.Id == message.Recipient.Id))
                {
                    Activity welcomeReply = message.CreateReply();
                    welcomeReply.Type = ActivityTypes.Message;
                    welcomeReply.Text = $"I'm the help desk bot and I can help you create a ticket or explore the knowledge base.\n" + "You can tell me things like _I need to reset my password_ or _explore hardware articles_.";
                    ConnectorClient connector = new ConnectorClient(new Uri(message.ServiceUrl));
                    connector.Conversations.ReplyToActivity(welcomeReply);
                }

            }
            else if (message.Type == ActivityTypes.ContactRelationUpdate)
            {
